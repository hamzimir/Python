# Lists are used to store multiple items in a single variable.
# Lists are one of 4 built-in data types in Python used to store collections of data, the other 3 are Tuple, Set, and Dictionary, all with different qualities and usage.
# Lists are created using square brackets [].
# Lists are ordered, changeable, and allow duplicate values.
# You can access list items by referring to the index number, starting at index 0.
# we can store different data types in a list, including strings, integers, and even other lists.

fruits = ["apple", "banana", "cherry"]
print(fruits)
print("The type of fruits is:", type(fruits))
print("The length of fruits is:", len(fruits))
print("The first fruit is:", fruits[0])
print("The last fruit is:", fruits[2])