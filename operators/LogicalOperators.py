# Logical Operators (Used to combine conditional statements)

# i-And operator (and: returns True if both statements are true)
p = True
q = False
print("p and q is:", p and q)

# ii-Or operator (or: returns True if one of the statements is true)
p = True
q = False
print("p or q is:", p or q)

# iii-Not operator (not: reverses the result)
p = True
print("not p is:", not p)