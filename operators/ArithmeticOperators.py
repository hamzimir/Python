# Arithmetic Operators (Used to perform common mathematical operations)

# i-Addition
a = 5
b = 10
sum = a + b
print("Sum is ::", sum)

# ii-Subtraction
sub = b - a
print("Subtraction is ::", sub)

# iii-Multiplication
mul = a * b
print("Multiplication is ::", mul)

# iv-Division
div = b / a
print("Division is ::", div)

# v-Modulus
mod = b % a
print("Modulus is ::", mod)

# vi-Exponentiation
exp = a ** 2
print("Exponentiation is ::", exp)

# vii-Floor Division
floor_div = b // a  
print("Floor Division is ::", floor_div)
