# Assignment operators (Used to assign values to variables)

# i-Assignment (# Assigning a value to a variable)
a = 10
print("a is ::", a)

# ii-addition Assignment (+=)
a = 10
a += 5
print("a is ::", a)

# iii-subtraction Assignment (-=)
b = 20
b -= 5
print("b is ::", b)         

# iv-multiplication Assignment (*=)
c = 10
c *= 5
print("c is ::", c)

# v-division Assignment (/=)
d = 20  
d /= 5
print("d is ::", d)

# vi-modulus Assignment (%=)
e = 25
e %= 4
print("e is ::", e)

# vii-exponentiation Assignment (**=)
f = 10
f **= 2
print("f is ::", f)

# viii-floor division Assignment (//=)
g = 20
g //= 5  
print("g is ::", g)       