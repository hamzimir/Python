# Membership Operators (are used to test if a sequence is presented in an object:)

# i-in operator (in: returns True if a sequence with the specified value is present in the object)
my_list = [1, 2, 3, 4, 5]
print(3 in my_list)

# ii-not in operator (not in: returns True if a sequence with the specified value is not present in the object)   
print(6 not in my_list) 
